#include "Sum.h"

int Sum::sum(int n1, int n2)
{
    int x = n1 + n2;
    return x;
}

int Sum::sum(int n1, int n2, int n3)
{
    x = n1 + n2 + n3;
    return x;
}

float Sum::sum(float n1, float n2)
{
    a = n1 + n2;
    return a;
}

string Sum::sum(string s1, string s2)
{
    q = s1 + " " + s2;
    return q;
}

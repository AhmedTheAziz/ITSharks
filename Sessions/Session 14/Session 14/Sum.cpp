#include "Sum.h"

int Sum::sum(int n1, int n2, int n3)
{
    return n1 + n2 + n3;
}

string Sum::sum(string s1, string s2)
{
    return s1 + " " + s2;
}

int Sum::sum(int n1, int n2)
{
    return n1 + n2;
}

int Sum::mul(int n1, int n2)
{

    return n1*n2;
}

int Sum::sub(int n1, int n2)
{
    return n1-n2;
}
